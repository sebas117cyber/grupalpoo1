A = float(input("Ingrese un numero para comparar: "))
B = float(input("Ingrese otro numero para comparar: "))

if A > B:
    print(f"El numero {A} es mayor que el numero {B}")
elif A < B:
    print(f"El numero {B} es mayor que el numero {A}")
else:
    print("Ambos numeros ingresados son iguales")